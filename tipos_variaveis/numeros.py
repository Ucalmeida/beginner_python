# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variável inteiro = ", type(numero_inteiro))
print("Tipo da variável real = ", type(numero_real))

# Soma
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtração
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# Multiplicação
multiplicacao = 2 * 2
print("2 * 2 = ", multiplicacao)

# Divisão
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variável do resultado divisão = ", type(divisao))
# Uma divisão sempre será do tipo float

# Converter um valor para inteiro - int()
print("Valor em inteiro = ", int(divisao))
# Sempre arredondando para baixo

# Converter um valor para float - float()
print("Valor em float = ", float(soma))

# Módulo %
modulo = 5 % 2  # Resultado é 1
print("Módulo = ", modulo)

# Divisão inteiro //
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variável do resultado divisão = ", type(divisao))
